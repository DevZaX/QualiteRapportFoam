package com.index;

public class PrixIndex {
	
	private Long id;
	private String ref;
	private Double priceA;
	private Double priceP;
	private Double poidsP;
	private String version;
	private String prototype;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getRef() {
		return ref;
	}
	public void setRef(String ref) {
		this.ref = ref;
	}
	public Double getPriceA() {
		return priceA;
	}
	public void setPriceA(Double priceA) {
		this.priceA = priceA;
	}
	public Double getPriceP() {
		return priceP;
	}
	public void setPriceP(Double priceP) {
		this.priceP = priceP;
	}
	public Double getPoidsP() {
		return poidsP;
	}
	public void setPoidsP(Double poidsP) {
		this.poidsP = poidsP;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getPrototype() {
		return prototype;
	}
	public void setPrototype(String prototype) {
		this.prototype = prototype;
	}
	
	

}
